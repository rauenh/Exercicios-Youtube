# Desafio 09 - Faça um programa que leia um número inteiro qualquer e mostre na tela a sua tabuada.

numero = int(input("Digite um número: "))
tabuada = (1*numero, 2*numero, 3*numero, 4*numero, 5*numero, 6*numero, 7*numero, 8*numero, 9*numero, 10*numero)
print("A tabuada do numero {} é: {} \n ".format(numero, tabuada))