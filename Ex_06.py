# Desafio 06 - Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada

numero = int(input("Digite um número: "))
dobro = 2*numero
triplo = 3*numero
raiz = (numero)**(1/2)

print("O dobro do número digitado é {}, o triplo do número digitado é {} e a raiz quadrada do número digitado é {}".format( dobro, triplo, raiz))
